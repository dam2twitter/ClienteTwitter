    private void cargarDatos() {
        jTextAreaTweet.setLineWrap(true);   //scroll horizontal desactivado
        jTextAreaTweet.setWrapStyleWord(true);  //evita la particiÃ³n de palabras
        jLabelScreenName.setText("@" + this.tweet.getUser().getScreenName());
        String texto = tweet.getText();
        int lineas = 0;
        String[] numLineas = texto.split("\n");
        lineas += numLineas.length;
        for (String numLinea : numLineas) {
            if (numLinea.length() > 30) {
                lineas += numLinea.length() / 30;
            }
        }
        jTextAreaTweet.setRows(lineas);
        jTextAreaTweet.setText(tweet.getText());
    }

    private void cargarImagenes() {
        try {
            //avatar
            ImageIcon avatar = new ImageIcon(new URL(tweet.getUser().getOriginalProfileImageURL()));
            jLabelAvatar.setSize(50, 50);
            Image avatarEscalado = avatar.getImage().getScaledInstance(jLabelAvatar.getWidth(), jLabelAvatar.getHeight(), Image.SCALE_DEFAULT);
            avatar.setImage(avatarEscalado);
            jLabelAvatar.setIcon(avatar);
            //multimedia
            if (tweet.getMediaEntities().length == 0) {
                jLabelImagen.setVisible(false);
            } else {
                MediaEntity mediaEntity = tweet.getMediaEntities()[0];
                if (mediaEntity.getType().equals("photo")) {
                    ImageIcon imagenTweet = new ImageIcon(new URL(mediaEntity.getMediaURL()));
                    double cocienteProporcion = (double)imagenTweet.getIconWidth() / 300;
                    int altoProporcionado = (int) Math.round((double) imagenTweet.getIconHeight() / cocienteProporcion);
                    Image imagenTweetEscalada = imagenTweet.getImage().getScaledInstance(300, altoProporcionado, Image.SCALE_DEFAULT);
                    imagenTweet.setImage(imagenTweetEscalada);
                    jLabelImagen.setIcon(imagenTweet);
                }
            }
        } catch (MalformedURLException ex) {
            ex.printStackTrace();
        }
    }


